DataRow _buildListItem(BuildContext context, DocumentSnapshot data) {
  final model = BranchModel.fromSnapshot(data);
  return DataRow(
      cells: [
    DataCell(Text(model.name)),
    DataCell(Text(model.phone)),
    DataCell(Text(model.location)),
        DataCell(Row(
          children: [
            IconButton(
              icon: Icon(Icons.delete_forever),
              color: Colors.white,
              onPressed: (){
                AwesomeDialog(
                  width: MediaQuery.of(context).size.width*0.3,
                  context: context,
                  dialogType: DialogType.QUESTION,
                  animType: AnimType.BOTTOMSLIDE,
                  dialogBackgroundColor: secondaryColor,
                  title: 'Delete Branch',
                  desc: 'Are you sure you want to delete this record?',
                  btnCancelOnPress: () {
                    Navigator.pushReplacement(context, MaterialPageRoute(builder: (BuildContext context) => MainScreen()));
                  },
                  btnOkOnPress: () {
                    FirebaseFirestore.instance.collection('branches').doc(model.id).delete().then((value) =>
                        Navigator.pushReplacement(context, MaterialPageRoute(builder: (BuildContext context) => MainScreen())));
                  },
                )..show();
              },
            ),
            IconButton(
              icon: Icon(Icons.edit),
              color: Colors.white,
              onPressed: (){
                _showEditBranch(model, context);
              },
            ),
          ],
        )),

  ]);
}


